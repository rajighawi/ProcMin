	public static java.awt.Color rndColor(){
		int n = colors.length;
		int i = (int)(Math.random() * n);
		return colors[i];
	}
	
	public static int distR(Color c1, Color c2){
		return Math.abs(c1.getRed()-c2.getRed());
	}
	
	public static int distG(Color c1, Color c2){
		return Math.abs(c1.getGreen()-c2.getGreen());
	}
	
	public static int distB(Color c1, Color c2){
		return Math.abs(c1.getBlue()-c2.getBlue());
	}
		
	public static boolean equal(Color c1, Color c2){
		return distR(c1, c2)+distG(c1, c2)+distB(c1, c2)==0;
	}
